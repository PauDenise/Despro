package com.example.maru.despro;

public class User {
    public String Name, Email, Cp, Age, Password, Verified;

    public User(String name, String age, String cp, String email, String password, String verified) {
        this.Name = name;
        this.Age = age;
        this.Cp = cp;
        this.Email = email;
        this.Password = password;
        this.Verified = verified;

}
}
